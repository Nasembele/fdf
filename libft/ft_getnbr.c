#include "libft.h"

int			ft_getnbr(char *str)
{
	int		i;
	int		nbr;
	int		key;

	i = 0;
	key = 1;
	nbr = 0;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			key = -1;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nbr = (nbr * 10) + (str[i] - '0');
		i++;
	}
	return (key * nbr);
}
